/// Custom Orb HTTP headers.
abstract class OrbHttpHeaders {
  /// The `origin` header.
  static const originHeader = 'origin';

  /// The `orb-access-token` header.
  static const orbAccessTokenHeader = 'orb-access-token';

  /// The `x-access-token` header.
  static const xAccessTokenHeader = 'x-access-token';

  /// The `orb-user` header.
  static const orbUserHeader = 'orb-user';

  /// The `orb-app-version` header.
  static const orbAppVersionHeader = 'orb-app-version';
}
